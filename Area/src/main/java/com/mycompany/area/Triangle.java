package com.mycompany.area;
public class Triangle {
    double tarea , tcircumference ;
        double area (double a , double b){
            tarea = 0.5 * a * b ;
            return tarea ;
        }
        double circumference (double a , double c , double d){
            tcircumference = a + c + d ;
            return tcircumference ;
        }
    
}
