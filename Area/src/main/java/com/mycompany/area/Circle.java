package com.mycompany.area;
public class Circle {
     double carea , ccircumference ;
        double area (double r){
            carea = 3.14 * r * r ;
            return carea ;
        }
        double circumference (double r){
            ccircumference = 2 * 3.14 * r ;
            return ccircumference ;
        }
    
}
